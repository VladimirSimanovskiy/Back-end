describe("mult", function() {

  it("Произведение положительных чисел", function() {
    assert.equal(mult('89756552767346', '65367286209'), '5867142273876316971331314');
  });

  it("Произведение отрицательных чисел", function() {
    assert.equal(mult('-874156946914', '-142412487120'), '124490864943248548747680');
  });

  it("Произведение положительного и отрицательного чисел", function() {
    assert.equal(mult('435234503474520', '-3414187120'), '-1485972035942301432182400');
  });

  it("Произведение отрицательного и положительного чисел", function() {
    assert.equal(mult('-35141251', '259987219'), '-9136276119670969');
  });

  it("Умножение числа на ноль", function() {
    assert.equal(mult('-874156946914', '0'), '0');
  });

  it("Умножение нуля на число", function() {
    assert.equal(mult('0', '3237257272372'), '0');
  });

  it("Произведение нулей", function() {
    assert.equal(mult('0', '0'), '0');
  });
});

describe("div", function() {

  it("Деление на единицу", function() {
    assert.equal(div('89756552767346', '1'), '89756552767346');
  });

  it("Деление на ноль", function() {
    assert.equal(div('89756552767346', '0'), 'InvalidValue: Делитель не может быть равен 0');
  });

  it("Деление меньшего числа на большее", function() {
    assert.equal(div('3515', '1516163'), '0 (остаток: 3515)');
  });

  it("Деление большего числа на меньшее (с остатком)", function() {
    assert.equal(div('41436314623', '14121'), '2934375 (остаток: 5248)');
  });

  it("Деление большего числа на меньшее (без остатка)", function() {
    assert.equal(div('21679350', '41294'), '525');
  });

  it("Деление положительного числа на отрицательное (с остатком)", function() {
    assert.equal(div('971215120240', '-60'), '-16186918670 (остаток: 40)');
  });

  it("Деление отрицательного числа на положительное (с остатком)", function() {
    assert.equal(div('-5919512', '1050'), '-5637 (остаток: 662)');
  });

  it("Деление положительного числа на отрицательное (без остатка)", function() {
    assert.equal(div('230174174552', '-877352'), '-262351');
  });

  it("Деление отрицательного числа на положительное (без остатка)", function() {
    assert.equal(div('-372776344', '2462'), '-151412');
  });

  it("Деление одинаковых чисел (одного знака)", function() {
    assert.equal(div('-2462234', '-2462234'), '1');
  });

  it("Деление одинаковых по модулю чисел (противоположных знаков)", function() {
    assert.equal(div('2462234', '-2462234'), '-1');
  });
});
