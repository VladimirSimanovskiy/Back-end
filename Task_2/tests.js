describe("add", function() {

  it("Сложение положительных чисел", function() {
    assert.equal(add('89756552767346', '65367286209'), '89821920053555');
  });

  it("Сложение отрицательных чисел", function() {
    assert.equal(add('-345452355153', '-23543462414'), '-368995817567');
  });

  it("Сложение положительного и отрицательного числа (результат - отрицательное число)", function() {
    assert.equal(add('24463246243', '-80210753298459'), '-80186290052216');
  });

  it("Сложение положительного и отрицательного числа (результат - положительное число)", function() {
    assert.equal(add('987970120', '-399612'), '987570508');
  });

  it("Сложение положительного и отрицательного числа (результат - ноль)", function() {
    assert.equal(add('24463246243', '-24463246243'), '0');
  });

  it("Сложение отрицательного и положительного числа (результат - отрицательное число)", function() {
    assert.equal(add('-98145989410', '58759273'), '-98087230137');
  });

  it("Сложение отрицательного и положительного числа (результат - положительное число)", function() {
    assert.equal(add('-72340114231', '23098503750171129'), '23098431410056898');
  });

  it("Сложение отрицательного и положительного числа (результат - ноль)", function() {
    assert.equal(add('-231414812341', '231414812341'), '0');
  });

  it("Сложение нулей", function() {
    assert.equal(add('0', '0'), '0');
  });
});

